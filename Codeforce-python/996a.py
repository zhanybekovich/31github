n = int(input())

money = [0, 0, 0, 0, 0]

rest = 0

for i in range(len(money)+1):
    if i == 0:
        money[i] = n // 100
        rest = n % 100
    elif i == 1:
        money[i] = rest // 20
        rest = rest % 20
    elif i == 2:
        money[i] = rest // 10
        rest = rest % 10
    elif i == 3:
        money[i] = rest // 5
        rest = rest % 5
    elif i == 4:
        money[i] = rest // 1
        rest = rest % 1

