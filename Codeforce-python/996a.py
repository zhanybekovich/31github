n = int(input())

# Номиналы купюр в убывающем порядке
denominations = [100, 20, 10, 5, 1]

# Переменная для хранения ответа (минимального числа купюр)
result = 0

# Проходим по каждому номиналу и вычитаем его из суммы, пока сумма не станет равной 0
for denom in denominations:
    # Целочисленное деление для получения максимального количества купюр данного номинала
    result += n // denom
    n %= denom  # Получаем остаток после вычитания купюр данного номинала

print(result)
